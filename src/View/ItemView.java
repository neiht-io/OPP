    private void btnDeleteItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteItemActionPerformed

        int selectedRow = ClothingTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn sản phẩm để xóa");
            return;
        }
        int itemId = (int) model.getValueAt(selectedRow, 1);

        if (new DAO().deleteItem(itemId)) {
            JOptionPane.showMessageDialog(this, "Xóa sản phẩm thành công");
            list = new DAO().getListItem();
            model.removeRow(selectedRow);
        } else {
            JOptionPane.showMessageDialog(this, "Xóa sản phẩm không thành công");

        }

    }//GEN-LAST:event_btnDeleteItemActionPerformed

