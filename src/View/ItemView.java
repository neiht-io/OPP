    private void BntAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntAddItemActionPerformed

        Item item = new Item();
        try {
            item.setItemId(Integer.parseInt(txtIItemId.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Mã sản phẩm chỉ nhận kí tự số và là duy nhất");

        }
        item.setItemName(txtItemName.getText());

        try {
            item.setSuplierId(Integer.parseInt(txtSuplierId.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Mã nhà cung cấp chỉ nhận kí tự số và là duy nhất");

        }
        if (new DAO().addItem(item)) {
            JOptionPane.showMessageDialog(rootPane, "Thêm sản phẩm thành công!");
            list.add(item);
            showResultItemView();
//           model.setRowCount(0);
        } else {

            JOptionPane.showMessageDialog(rootPane, "Thêm sản phẩm thất bại,vui lòng kiểm tra lại thông tin!");

        }
        
//        showResultItemView();


    }//GEN-LAST:event_BntAddItemActionPerformed

