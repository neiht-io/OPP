    public void showResultOutputView() {

        Output output = list.get(list.size() - 1);
        model.addRow(new Object[]{
            i++, output.getOutputId(), output.getOutputDate()

        });

    }
    private void btnDeleteOutputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteOutputActionPerformed
        int selectedRow = OutputTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn để xóa");
            return;
        }
        int ouputId = (int) model.getValueAt(selectedRow, 1);
        
        if (new DAO().deleteOutput(ouputId)) {
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            list = new DAO().getListOutnput();
            model.removeRow(selectedRow);
        } else {
            JOptionPane.showMessageDialog(this, "Xóa sản phẩm không thành công");

        }
    }//GEN-LAST:event_btnDeleteOutputActionPerformed

