    private void btnAddOutputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddOutputActionPerformed
        Output output = new Output();
        if (txtOutputtId.getText().equals("") || txtOutputDate.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đủ thông tin");
        } else {
            try {
                output.setOutputId(Integer.parseInt(txtOutputtId.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Mã phiếu xuất chỉ nhận kí tự số và là duy nhất");

            }
            try {
                output.setOutputDate(new SimpleDateFormat("dd/MM/yyyy").parse(txtOutputDate.getText()));

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Ngày tạo không hợp lệ ");
            }
            if (new DAO().addOutput(output)) {
                JOptionPane.showMessageDialog(rootPane, "Thêm phiếu xuất thành công!");
                list.add(output);
                showResultOutputView();
//           model.setRowCount(0);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Thêm phiếu nhập thất bại,vui lòng kiểm tra lại thông tin!");
            }
        }
    }//GEN-LAST:event_btnAddOutputActionPerformed
