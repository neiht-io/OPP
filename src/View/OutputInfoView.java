    private void btnDeleteInputInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteInputInfoActionPerformed
          int selectedRow = OutputInfoTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn phiếu xuất sản phẩm để xóa");
            return;
        }
        int outputInfoId = (int) model.getValueAt(selectedRow, 1);

        if (new DAO().deleteInputInfoId(outputInfoId)) {
            JOptionPane.showMessageDialog(this, "Xóa phiếu xuất sản phẩm thành công");
            list = new DAO().getListOutputInfo();
            model.removeRow(selectedRow);
        } else {
            JOptionPane.showMessageDialog(this, "Xóa phiếu xuất sản phẩm không thành công");}
    }//GEN-LAST:event_btnDeleteInputInfoActionPerformed

