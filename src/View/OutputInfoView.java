    private void btnAddinputInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddinputInfoActionPerformed
        OutPutInfo outputInfo = new OutPutInfo();
        if (txtOuputInfoId.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng đủ thông tin");
        } else {
            try {
                outputInfo.setOutputInfoId(Integer.parseInt(txtOuputInfoId.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Mã xuất sản phẩm chỉ nhận kí tự số và là duy nhất");
            }
            try {
                outputInfo.setItemId(Integer.parseInt(txtitemId.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập mã sản phẩm là số");
            }

            try {
                outputInfo.setInputInforId(Integer.parseInt(txtInputInfoId.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập mã nhập sản phẩm là số");
            }
            try {
                outputInfo.setOutputId(Integer.parseInt(txtOutputId.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập mã phiếu suất sản phẩm là số");
            }
            try {
                outputInfo.setQuantity(Float.parseFloat(txtQuantity.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập số lượng sản phẩm là số");
            }
            try {
                outputInfo.setCustomerId(Integer.parseInt(txtcustomerId.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập mã khách hàng sản phẩm là số");
            }
            try {
                outputInfo.setOutputInfoDate(new SimpleDateFormat("dd/MM/yyyy").parse(txtOutputInfoDate.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đúng định dạnh thời gian xuất");
            }
            outputInfo.setOuputInfoStatus(txtStatis.getText());

            if (new DAO().addOutputInfo(outputInfo)) {
                JOptionPane.showMessageDialog(rootPane, "Thêm phiếu xuất sản phẩm thành công!");
                list.add(outputInfo);
                showResultOutputInfoView();

            } else {
                JOptionPane.showMessageDialog(rootPane, "Thêm phiếu xuất sản phẩm thất bại,vui lòng kiểm tra lại thông tin!");
            }

        }


    }//GEN-LAST:event_btnAddinputInfoActionPerformed

